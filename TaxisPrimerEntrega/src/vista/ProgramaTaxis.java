    private void btnUsuariosFrecuentesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsuariosFrecuentesActionPerformed

        try {
            String n = JOptionPane.showInputDialog(VentanaMostarUsuarios, "Ingrese la frecuencia de viajes para buscar usuarios frecuentes");
            int noServicios = Integer.parseInt(n);
            int vecContadorUsuarios[] = new int[listUsuarios.size()];

            int contadorUsuarios = 0;

            for (int i = 0; i < listUsuarios.size(); i++) {
                int t = listUsuarios.get(i).getIDUsuario();
                contadorUsuarios = 0;
                for (int j = 0; j < listServicios.size(); j++) {
                    int s = listServicios.get(j).getIDUser();
                    if (t == s) {
                        contadorUsuarios++;
                        vecContadorUsuarios[i] = contadorUsuarios;
                    }
                }
            }

            boolean coincidencia = false;
            ArrayList<Usuario> usuariosEncontrados = new ArrayList();
            DefaultListModel listModelUsuariosEncontrados = new DefaultListModel();
            for (int i = 0; i < vecContadorUsuarios.length; i++) {

                if (vecContadorUsuarios[i] >= noServicios) {
                    coincidencia = true;
                    usuariosEncontrados.add((Usuario) listUsuarios.get(i));

                    //JOptionPane.showMessageDialog(VentanaTaxis, "para la placa:   " + listTaxis.get(i).getPlaca() + "  hay   " + vecContadorTaxis[i] + "   Servicios");
                }
            }
            if (!coincidencia) {
                JOptionPane.showMessageDialog(VentanaTaxis, "No hay usuarios que tengan concidencia de:   " + noServicios + "  Servicios");
            } else {

                listModelUsuariosEncontrados.removeAllElements();
                for (Usuario u : usuariosEncontrados) {
                    listModelUsuariosEncontrados.addElement(u);
                }
                jListUsuarios.setModel(listModelUsuariosEncontrados);
            }

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(VentanaTaxis, "Ingrese un numero v√°lido", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUsuariosFrecuentesActionPerformed

    private void btnViualizarViajesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViualizarViajesActionPerformed

        VentanaMostrarServicios.setLocationRelativeTo(null);
        
        //VentanaMostrarServicios.setAlwaysOnTop(true);
        VentanaMostrarServicios.setVisible(true);
        try {
            int index = jListUsuarios.getSelectedIndex();
            Usuario p = (Usuario) listmodelUsuarios.get(index);
            int IDUSER = p.getIDUsuario();

            boolean coincidencia = false;
            ArrayList<Servicio> serviciosEncontrados = new ArrayList();
            DefaultListModel listModelserviciosEncontrados = new DefaultListModel();
            for (int i = 0; i < listServicios.size(); i++) {
                if (IDUSER == listServicios.get(i).getIDUser()) {
                    coincidencia = true;
                    serviciosEncontrados.add(listServicios.get(i));
                }
            }
            if (!coincidencia) {
                JOptionPane.showMessageDialog(VentanaNuevoUsuario, "No hay usuarios con estos servicios");
            } else {
                listModelserviciosEncontrados.removeAllElements();
                for (int i = 0; i < serviciosEncontrados.size(); i++) {
                    listModelserviciosEncontrados.addElement(serviciosEncontrados.get(i));
                }
                jListServicios.setModel(listModelserviciosEncontrados);
            }

           
            
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(VentanaTaxis, "Debe seleccionar un usuario para visualizar un nuevo Servicio", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_btnViualizarViajesActionPerformed

